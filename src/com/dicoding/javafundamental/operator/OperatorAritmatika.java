package com.dicoding.javafundamental.operator;

public class OperatorAritmatika {
    public static void main(String[] args) {
        System.out.println("Operasi Penjumlahan");
        int hasilPenjumlahan = 5 + 1;
        System.out.println("Hasil 5 + 1 = " + hasilPenjumlahan);
        System.out.println();

        System.out.println("Operasi Pengurangan");
        int hasilPengurangan = 5 - 1;
        System.out.println("Hasil 5 - 1 = " + hasilPengurangan);
        System.out.println();

        System.out.println("Operasi Perkalian");
        int hasilPerkalian = 5 * 2;
        System.out.println("Hasil 5 x 2 = " + hasilPerkalian);
        System.out.println();

        System.out.println("Operasi Pembagian");
        int hasilPembagian = 4 / 2;
        System.out.println("Hasil 4 / 2 = " + hasilPembagian);
        System.out.println();

        System.out.println("Operasi Habis Bagi");
        int habisSisa = 5 % 2;
        System.out.println("Hasil 5 % 2 = " + habisSisa);
        int habisSisaLain = 10 % 4;
        System.out.println("Hasil 10 % 4 = " + habisSisaLain);
    }
}
